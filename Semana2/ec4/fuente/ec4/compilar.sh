#######################################################################
# Ruta actual.														  #
#######################################################################
ACTUAL=`pwd` 

#######################################################################
# Ruta de bin.														  #
#######################################################################
cd ../../bin 
BIN=`pwd` 
cd $ACTUAL

#######################################################################
# Ruta de lib.														  #
#######################################################################
cd ../../lib 
LIB=`pwd` 
cd $ACTUAL

#######################################################################
# Ruta de incl.														  #
#######################################################################
cd ../incl
INCL=`pwd` 
cd $ACTUAL

#######################################################################
# Ruta de modulo grado 0.											  #
#######################################################################
cd ../grado0
GRADO0=`pwd`
cd $ACTUAL

#######################################################################
# Ruta de modulo grado 1.											  #
#######################################################################
cd ../grado1
GRADO1=`pwd`
cd $ACTUAL

#######################################################################
# Ruta de modulo grado 2.											  #
#######################################################################
cd ../grado2
GRADO2=`pwd`
cd $ACTUAL

#######################################################################
# Ruta de modulo grado 3.											  #
#######################################################################
cd ../grado3
GRADO3=`pwd`
cd $ACTUAL

#######################################################################
# Ruta de modulo grado 4.											  #
#######################################################################
cd ../grado4
GRADO4=`pwd`
cd $ACTUAL

#######################################################################
# Compilar.															  #
#######################################################################
make $* RUTA_BIN=$BIN/ RUTA_LIB=$LIB/ RUTA_INCL=$INCL/ \
		RUTA_GRADO0=$GRADO0/ RUTA_GRADO1=$GRADO1/ RUTA_GRADO2=$GRADO2/ \
		RUTA_GRADO3=$GRADO3/ RUTA_GRADO4=$GRADO4/
