# Ruta de incl.                                                       # 
####################################################################### 
cd ../incl 
INCL=`pwd` 
cd $ACTUAL 
 
####################################################################### 
# Compilar.                                                           # 
####################################################################### 
make $* RUTA_LIB=$LIB/ RUTA_INCL=$INCL/