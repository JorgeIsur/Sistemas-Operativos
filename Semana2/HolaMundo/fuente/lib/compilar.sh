####################################################################### 
 
####################################################################### 
# Ruta actual.                                                        # 
####################################################################### 
ACTUAL=`pwd` 
 
####################################################################### 
# Ruta de lib.                                                        # 
#######################################################################
cd ../../lib
LIB=`pwd` 
