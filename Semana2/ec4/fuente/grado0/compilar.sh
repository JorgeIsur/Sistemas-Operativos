#!/bin/ksh
#######################################################################
# Shell script para compilar el modulo grado 0						  #
# $Id: compilar.sh,v 1.4 2018/10/04 01:03:13 hpl Exp $  			  #
# $Author: hpl $													  #
#######################################################################

#######################################################################
# Ruta actual.														  #
#######################################################################
ACTUAL=`pwd` 

#######################################################################
# Ruta de bin.														  #
#######################################################################
cd ../../bin 
BIN=`pwd` 
cd $ACTUAL

#######################################################################
# Ruta de lib.														  #
#######################################################################
cd ../../lib 
LIB=`pwd` 
cd $ACTUAL

#######################################################################
# Ruta de incl.														  #
#######################################################################
cd ../incl
INCL=`pwd` 
cd $ACTUAL

#######################################################################
# Compilar.															  #
#######################################################################
make $* RUTA_BIN=$BIN/ RUTA_LIB=$LIB/ RUTA_INCL=$INCL/
